<?php
//Написать функцию, которая принимает на вход число, а затем возвращает булевский ответ - простое ли оно.
//Для всех i от 1 до 10 { проверить, делится ли число i на какое-либо из чисел до него если делится, то это i
// не подходит, берём следующее если не делится, то i - простое число

function isPrime(int $num): bool {
  if ($num <= 1) {
    return false; // Числа меньше или равные 1 не являются простыми
  }

  for ($i = 2; $i <= sqrt($num); $i++) { // Проверяем делимость от 2 до корня из числа
    if ($num % $i === 0) {
      return false; // Если делится на i, то число не является простым
    }
  }

  return true; // Если ни на что не делится, то число простое
}

for ($i = 1; $i <= 10; $i++) {
  echo "$i: " . (isPrime($i) ? 'простое' : 'не простое') . "\n";
}